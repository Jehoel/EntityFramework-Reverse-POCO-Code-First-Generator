                    _preHeaderInfo.AppendLine(string.Format("//     Configuration file:     \"{0}\"", Settings.ConfigFilePath));
                    _preHeaderInfo.AppendLine(string.Format("//     Connection String Name: \"{0}\"", Settings.ConnectionStringName));
                }
                _preHeaderInfo.AppendLine(string.Format("//     Connection String:      \"{0}\"", ZapPassword()));

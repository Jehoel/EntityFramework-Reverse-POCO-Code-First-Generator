            _headerInfo.AppendLine("#pragma warning disable 1591    //  Ignore \"Missing XML Comment\" warning");
            _headerInfo.AppendLine();
        }

        public virtual void Init()
        {
        }

        private string ZapPassword()
        {
            var rx = new Regex("password=[^\";]*", RegexOptions.Singleline | RegexOptions.Multiline | RegexOptions.IgnoreCase);
            return rx.Replace(Settings.ConnectionString, "password=**zapped**;");
        }

        public void Test()
        {
            Header();

            if (Settings.ElementsToGenerate.HasFlag(Elements.Interface) && !string.IsNullOrWhiteSpace(Settings.DbContextInterfaceName))
                Interface();

            DatabaseContext();
            DatabaseContextFactory();
            FakeContextFactory();
            PocoClasses();
            PocoConfiguration();
            StoredProcedureReturnModels();

            Footer();
        }

        public void AppendPreHeaderInfo(string databaseDetails)
        {
            _preHeaderInfo.Append(databaseDetails);
        }

        public void PreHeaderInfo()
        {
            _outer.WriteLine(_preHeaderInfo.ToString());
        }

        public virtual void Header()
        {
            PreHeaderInfo();
            _outer.WriteLine(_headerInfo.ToString());
        }

        public virtual void Footer()
        {
            _outer.WriteLine("}");
            _outer.WriteLine("// </auto-generated>");
        }

        public void ConnectionSettingComments()
        {

        }

        public bool IsSupportedFrameworkVersion(string frameworkVersion)
        {
            if (_supportedFrameworkVersionCache.ContainsKey(frameworkVersion))
                return _supportedFrameworkVersionCache[frameworkVersion];

            var nfi = CultureInfo.InvariantCulture.NumberFormat;
            var isSupported = float.Parse(frameworkVersion, nfi);
            var result = isSupported <= Settings.TargetFrameworkVersion;
            _supportedFrameworkVersionCache.Add(frameworkVersion, result);
            return result;
        }

